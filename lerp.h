#ifndef LERP_H
#define LERP_H

/*
 * LERP
 * --------------
 * @brief Basic Linear Interpolation Function
 * 
 * @param x the input scalar
 * @param x1 Point A's 'X' value
 * @param x2 Point B's 'X' value
 * @param y1 Point A's 'Y' value
 * @param y2 Point B's 'Y' value
 * 
 * @return the interpolated value
*/
float lerp(float x, float x1, float x2, float y1, float y2);

#endif